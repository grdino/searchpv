/*
Community description seed for SearchPV content.page_content.

IMPORTANT: community names are not globally unique. This script changes content uniqueness
so community descriptions are unique by content_type + entity_type + zone_slug + area_slug + entity_slug + sort_order.

Six duplicate community_slug values: buenos-aires,emiliano-zapata,las-gaviotas, los-mangos, oceanside, other
So when you display community descriptions, query like this:

.eq("content_type", "description")
.eq("entity_type", "community")
.eq("zone_slug", row.zone_slug)
.eq("area_slug", row.area_slug)
.eq("community_slug", row.community_slug)

That avoids showing the Punta de Mita Emiliano Zapata description on the Puerto Vallarta Emiliano Zapata page, etc.
*/

